#include "push_swap.h"

void	sort_3(t_stack **a, t_stack **b)
{
	if (!(*b))
	{
		if (ref(*a) == 2)
			ra(a);
		else if (ref((*a)->next) == 2)
			rra(a);
		if (ref(*a) == 1)
			sa(*a);
	}
}
